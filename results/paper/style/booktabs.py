"""Shared booktabs + CSV table writer (artefatos.md §14.3)."""

from __future__ import annotations

import csv
from pathlib import Path
from typing import Any, Sequence


def _cell_tex(text: str, *, bold: bool = False, italic: bool = False) -> str:
    s = str(text)
    if bold and italic:
        return rf"\textbf{{\textit{{{s}}}}}"
    if bold:
        return rf"\textbf{{{s}}}"
    if italic:
        return rf"\textit{{{s}}}"
    return s


def to_booktabs(
    columns: Sequence[str],
    rows: Sequence[Sequence[Any]],
    *,
    bold_mask: Sequence[Sequence[bool]] | None = None,
    italic_mask: Sequence[Sequence[bool]] | None = None,
    row_color: Sequence[str | None] | None = None,
    midrule_before: Sequence[int] | None = None,
    preamble: str = "",
    table_note: str = "",
    col_spec: str | None = None,
) -> str:
    """Build a booktabs fragment ready for \\input{} (no floating table env)."""
    n_col = len(columns)
    if col_spec is None:
        col_spec = "l" * n_col
    lines: list[str] = []
    if preamble:
        lines.append(preamble.rstrip())
    lines.append(rf"\begin{{tabular}}{{{col_spec}}}")
    lines.append(r"\toprule")
    lines.append(" & ".join(_cell_tex(c) for c in columns) + r" \\")
    lines.append(r"\midrule")
    mid = set(midrule_before or [])
    for i, row in enumerate(rows):
        if i in mid:
            lines.append(r"\midrule")
        if row_color and row_color[i]:
            lines.append(rf"\rowcolor{{{row_color[i]}}}")
        cells = []
        for j, val in enumerate(row):
            b = bool(bold_mask[i][j]) if bold_mask else False
            it = bool(italic_mask[i][j]) if italic_mask else False
            cells.append(_cell_tex(val, bold=b, italic=it))
        lines.append(" & ".join(cells) + r" \\")
    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    if table_note:
        lines.append("")
        lines.append(rf"\smallskip\noindent\textit{{{table_note}}}")
    return "\n".join(lines) + "\n"


def write_table(
    out_stem: Path,
    columns: Sequence[str],
    rows: Sequence[Sequence[Any]],
    *,
    bold_mask: Sequence[Sequence[bool]] | None = None,
    italic_mask: Sequence[Sequence[bool]] | None = None,
    row_color: Sequence[str | None] | None = None,
    midrule_before: Sequence[int] | None = None,
    preamble: str = "",
    table_note: str = "",
    col_spec: str | None = None,
    caption: str = "",
) -> tuple[Path, Path]:
    """Write ``.tex`` (booktabs) and ``.csv`` side by side. Returns (tex, csv)."""
    out_stem.parent.mkdir(parents=True, exist_ok=True)
    tex_path = out_stem.with_suffix(".tex")
    csv_path = out_stem.with_suffix(".csv")

    body = to_booktabs(
        columns,
        rows,
        bold_mask=bold_mask,
        italic_mask=italic_mask,
        row_color=row_color,
        midrule_before=midrule_before,
        preamble=preamble,
        table_note=table_note,
        col_spec=col_spec,
    )
    header = "% Auto-generated by Block P — do not edit by hand.\n"
    if caption:
        header += f"% Caption: {caption}\n"
    tex_path.write_text(header + body, encoding="utf-8")

    with csv_path.open("w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(columns)
        for row in rows:
            w.writerow([_strip_tex(str(c)) for c in row])
    return tex_path, csv_path


def _strip_tex(s: str) -> str:
    """Flatten TeX cells for CSV without destroying scientific notation / checkmarks."""
    import re

    s = str(s)
    s = s.replace(r"\checkmark", "Yes")
    s = s.replace(r"\times", "×")
    s = s.replace(r"\&", "&")
    s = s.replace(r"\_", "_")
    # unwrap one-argument TeX commands
    for _ in range(3):
        s2 = re.sub(r"\\(?:textbf|textit|texttt)\{([^{}]*)\}", r"\1", s)
        if s2 == s:
            break
        s = s2
    s = s.replace("$", "")
    s = re.sub(r"10\^\{(-?\d+)\}", r"10^\1", s)
    s = s.replace("{", "").replace("}", "")
    return s
